'''
所谓的引用变量就是指：变量与值之间存在一种引用关系
在Python代码中，我们可以使用id()来查看变量所指向的内存空间
'''
a = 10
print(a)  # 10
print(id(a))  # 140712685676608

# 如果定义一个新变量b，把变量a赋值给变量b，两者内存有何关系
b = a
print(id(b))  # 140712685676608

