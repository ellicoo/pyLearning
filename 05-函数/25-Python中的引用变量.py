# 疑问：明明a和b指向相同的内存地址，为什么a发生了改变，b没有随之改变呢？
# 答：数字类型属于不可变类型，数据区中内存地址一旦固定，其内部的值就不能发生改变了，如果需要改变，必须重新开辟空间

# 结论：在计算机底层，如果内存地址一旦固定，其值无法发生改变，则这种数据类型就是不可变类型
# 在Python中，数字类型、布尔类型、字符串类型、元组类型都属于不可变类型
